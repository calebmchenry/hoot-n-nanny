export const GLYPH_WIDTH = 5;
export const GLYPH_HEIGHT = 7;
export const GLYPH_SPACING = 1;

export const ASCII_PRINTABLE = Array.from({ length: 95 }, (_, index) => {
  return String.fromCharCode(32 + index);
}).join('');

type GlyphRows = [string, string, string, string, string, string, string];

const rowsToColumns = (rows: GlyphRows): number[] => {
  const columns: number[] = [];
  for (let x = 0; x < GLYPH_WIDTH; x += 1) {
    let columnByte = 0;
    for (let y = 0; y < GLYPH_HEIGHT; y += 1) {
      if (rows[y][x] === '#') {
        columnByte |= 1 << y;
      }
    }
    columns.push(columnByte);
  }
  return columns;
};

