const getRowsForChar = (char: string): GlyphRows => {
  const direct = GLYPH_ROWS[char];
  if (direct) {
    return direct;
  }

  const lower = lowerCaseOverrides[char];
  if (lower) {
    return lower;
  }

  if (char >= 'a' && char <= 'z') {
    return GLYPH_ROWS[char.toUpperCase()] ?? GLYPH_ROWS['?'];
  }

  return GLYPH_ROWS['?'];
};

const glyphData: Record<number, number[]> = {};
for (let code = 32; code <= 126; code += 1) {
  glyphData[code] = rowsToColumns(getRowsForChar(String.fromCharCode(code)));
}

export const GLYPH_DATA: Record<number, number[]> = glyphData;
